-- ============================================================================
-- 50_sim_adjudication.sql — the SIMULATED adjudication layer (CLAUDE.md §3)
--
-- Grain:        sim_payer                     : one simulated payer archetype
--               sim_service_line              : one simulated service-line bucket
--               sim_authorization_eligibility : one claim (pre-submission facts)
--               sim_documentation_coding      : one claim (pre-submission facts)
--               sim_claim_adjudication        : one claim (outcome + money + dates)
--               sim_appeals                   : one (claim, appeal level)
--               sim_workflow_events           : one (claim, event occurrence)
--               sim_operating_costs           : one claim
--
-- Sources:      GENERATED by src/simulation/ from config/simulation.yaml.
--               Conditioned on SOURCE claim attributes (charges, DRG, dates,
--               diagnosis count) but no SOURCE value is copied into a sim_
--               column — billed charges stay in rcm.fact_inpatient_claim and are
--               reached by joining on claim_sk.
--
-- Provenance:   SIMULATED, every table and every column (§3.1, §3.2). The CMS
--               synthetic claims contain NO denials, NO submission/adjudication
--               dates, NO appeals and NO workflow events; all of that is
--               invented here. Nothing in these tables describes real Medicare,
--               Medicare Advantage, commercial or Medicaid adjudication
--               behaviour. Calibration anchors: docs/assumptions.md.
--
-- Owner:        simulation-engineer (§5). Loaded by src/simulation/load_sim.py
--               (`make simulate-warehouse`), NOT by the Phase 1 loader.
--
-- ORDERING:     these tables carry foreign keys to rcm.fact_inpatient_claim.
--               `make warehouse` drops that table with CASCADE, which silently
--               drops those constraints. Correct order is always:
--                   make warehouse  ->  make simulate  ->  make simulate-warehouse
--               The loader refuses to run if the fact table is absent.
--
-- Notes:        sim_ prefix on every identifier except claim_sk / clm_id, which
--               are the warehouse's DERIVED surrogate and SOURCE degenerate key
--               — renaming those would misrepresent their provenance.
--               Idempotent drop/recreate. PostgreSQL 16.
-- ============================================================================

-- ---------------------------------------------------------------------------
-- sim_payer — the simulated multi-payer dimension (CLAUDE.md §3.5).
-- Medicare FFS has ONE payer; every payer below is an invented ARCHETYPE, not
-- modelled on or named after any real insurer.
-- ---------------------------------------------------------------------------
drop table if exists rcm.sim_payer cascade;
create table rcm.sim_payer (
    sim_payer_id           text primary key,
    sim_payer_name         text not null,
    sim_payer_mix_share    numeric(6, 4) not null,
    sim_timely_filing_days integer not null,
    sim_provenance         text not null default 'SIMULATED',
    sim_config_version     text not null,
    sim_seed               bigint not null,
    constraint ck_sim_payer_mix check (sim_payer_mix_share between 0 and 1),
    constraint ck_sim_payer_filing check (sim_timely_filing_days > 0)
);
-- NOTE: this file must contain no literal percent-sign character anywhere,
-- including in comments. psycopg2 reads one as a parameter placeholder and the
-- entire DDL script then fails to execute. Write "percent" or "share" instead.
comment on table rcm.sim_payer is
  'SIMULATED: invented payer archetypes. Medicare FFS has one payer; this '
  'multi-payer dimension is entirely simulated (CLAUDE.md §3.5).';

-- ---------------------------------------------------------------------------
-- sim_service_line — coarse MS-DRG range buckets. The boundaries are a design
-- choice OF THIS SIMULATION, not an official CMS MS-DRG/MDC taxonomy, which is
-- why the column is SIMULATED even though its input (drg_cd) is SOURCE.
-- ---------------------------------------------------------------------------
drop table if exists rcm.sim_service_line cascade;
create table rcm.sim_service_line (
    sim_service_line_id   text primary key,
    sim_service_line_name text not null,
    sim_drg_range_lo      integer,          -- null on the UNKNOWN (no-DRG) member
    sim_drg_range_hi      integer,
    sim_provenance        text not null default 'SIMULATED',
    sim_config_version    text not null,
    sim_seed              bigint not null,
    constraint ck_sim_sl_range check (
        sim_drg_range_lo is null or sim_drg_range_hi is null
        or sim_drg_range_lo <= sim_drg_range_hi
    )
);
comment on table rcm.sim_service_line is
  'SIMULATED: service-line buckets defined by this project as MS-DRG numeric '
  'ranges. NOT an official CMS grouping.';

-- ---------------------------------------------------------------------------
-- sim_authorization_eligibility — PRE-SUBMISSION facts. Everything here is
-- knowable before a claim goes out the door, so these ARE legitimate features
-- for the Model A denial-risk problem (CLAUDE.md §4).
-- ---------------------------------------------------------------------------
drop table if exists rcm.sim_authorization_eligibility cascade;
create table rcm.sim_authorization_eligibility (
    claim_sk                    bigint primary key
        references rcm.fact_inpatient_claim (claim_sk),
    clm_id                      text not null,
    sim_payer_id                text not null references rcm.sim_payer (sim_payer_id),
    sim_auth_required           boolean not null,
    sim_auth_obtained           boolean not null,
    sim_auth_missing            boolean not null,
    sim_auth_obtained_late      boolean not null,
    sim_auth_reference_id       text,           -- invented id, not a real auth number
    sim_auth_request_date       date,
    sim_auth_decision_date      date,
    sim_eligibility_checked     boolean not null,
    sim_eligibility_failed      boolean not null,
    sim_eligibility_check_date  date,
    sim_secondary_payer_present boolean not null,
    sim_provenance              text not null default 'SIMULATED',
    sim_config_version          text not null,
    sim_seed                    bigint not null,
    -- An authorization cannot be both obtained and missing, and neither can be
    -- true when none was required.
    constraint ck_sim_auth_exclusive check (not (sim_auth_obtained and sim_auth_missing)),
    constraint ck_sim_auth_requires_required check (
        sim_auth_required or (not sim_auth_obtained and not sim_auth_missing)
    ),
    constraint ck_sim_elig_requires_check check (sim_eligibility_checked or not sim_eligibility_failed)
);
create index ix_sim_auth_payer on rcm.sim_authorization_eligibility (sim_payer_id);
comment on table rcm.sim_authorization_eligibility is
  'SIMULATED pre-submission authorization/eligibility facts. Invented; the CMS '
  'synthetic claims contain no authorization or eligibility data.';

