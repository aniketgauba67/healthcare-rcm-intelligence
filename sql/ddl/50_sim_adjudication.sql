-- ---------------------------------------------------------------------------
-- sim_claim_adjudication — the hub. One row per claim: generated timeline,
-- generated money, generated denial outcome.
--
-- LEAKAGE WARNING (CLAUDE.md §4): almost every column here is POST-SUBMISSION
-- and must be on the Model A forbidden list — the outcome, all money, all
-- adjudication/payment dates. sim_latent_p and sim_provider_quality_latent are
-- generator internals stored for VALIDATION ONLY and must never be features.
-- The authoritative list is docs/simulated_forbidden_columns.md.
-- The exceptions (legitimately pre-submission): sim_payer_id,
-- sim_service_line_id, sim_coded_date, sim_submission_date,
-- sim_filing_limit_days, sim_days_service_to_submission, sim_late_filing_flag.
-- ---------------------------------------------------------------------------
drop table if exists rcm.sim_claim_adjudication cascade;
create table rcm.sim_claim_adjudication (
    claim_sk                          bigint primary key
        references rcm.fact_inpatient_claim (claim_sk),
    clm_id                            text not null unique,
    sim_payer_id                      text not null references rcm.sim_payer (sim_payer_id),
    sim_service_line_id               text not null
        references rcm.sim_service_line (sim_service_line_id),
    -- ---- timeline (all generated; the source has service dates only) ----
    sim_coded_date                    date not null,
    sim_submission_date               date not null,
    sim_ack_date                      date not null,
    sim_adjudication_date             date not null,
    sim_denial_review_date            date,          -- denied claims only
    sim_payment_date                  date,          -- claims with a payment only
    sim_filing_limit_days             integer not null,
    sim_days_service_to_submission    integer not null,
    sim_days_to_adjudication          integer not null,
    sim_days_to_payment               integer,
    sim_late_filing_flag              boolean not null,
    -- ---- money (allowed/paid/adjustment; billed stays in the SOURCE fact) ----
    sim_allowed_amount                numeric(14, 2) not null,
    sim_paid_amount                   numeric(14, 2) not null,
    sim_patient_responsibility_amount numeric(14, 2) not null,
    sim_contractual_adjustment_amount numeric(14, 2) not null,
    sim_denied_amount                 numeric(14, 2) not null,
    -- ---- outcome ----
    sim_denial_flag                   boolean not null,
    sim_denial_type                   text not null,  -- NONE | FULL | PARTIAL
    sim_denial_category               text,           -- catalog id; null iff not denied
    sim_denial_carc_group             text,           -- CARC code as a LABEL only (§3.7)
    sim_denial_driver_mechanism       text,           -- strongest contributing mechanism
    -- ---- generator internals: VALIDATION ONLY, never a model feature ----
    sim_latent_p                      numeric(10, 6) not null,
    sim_label_noise_applied           boolean not null,
    sim_provider_quality_latent       numeric(10, 6) not null,
    sim_provenance                    text not null default 'SIMULATED',
    sim_config_version                text not null,
    sim_seed                          bigint not null,
    -- Money invariants. paid <= allowed is enforced here; allowed <= billed
    -- needs the SOURCE fact table and is checked by the validation suite.
    constraint ck_sim_adj_nonneg check (
        sim_allowed_amount >= 0 and sim_paid_amount >= 0
        and sim_patient_responsibility_amount >= 0
        and sim_contractual_adjustment_amount >= 0 and sim_denied_amount >= 0
    ),
    constraint ck_sim_adj_paid_le_allowed check (sim_paid_amount <= sim_allowed_amount),
    constraint ck_sim_adj_latent_p check (sim_latent_p > 0 and sim_latent_p < 1),
    constraint ck_sim_adj_denial_type check (sim_denial_type in ('NONE', 'FULL', 'PARTIAL')),
    -- A denial has a category and a type; a paid claim has neither.
    constraint ck_sim_adj_category_iff_denied check (
        (sim_denial_flag and sim_denial_category is not null and sim_denial_type <> 'NONE')
        or (not sim_denial_flag and sim_denial_category is null and sim_denial_type = 'NONE')
    ),
    constraint ck_sim_adj_date_order check (
        sim_coded_date <= sim_submission_date
        and sim_submission_date <= sim_ack_date
        and sim_ack_date <= sim_adjudication_date
        and (sim_payment_date is null or sim_payment_date >= sim_adjudication_date)
        and (sim_denial_review_date is null or sim_denial_review_date >= sim_adjudication_date)
    )
);
create index ix_sim_adj_payer     on rcm.sim_claim_adjudication (sim_payer_id);
create index ix_sim_adj_sl        on rcm.sim_claim_adjudication (sim_service_line_id);
create index ix_sim_adj_denial    on rcm.sim_claim_adjudication (sim_denial_flag);
create index ix_sim_adj_category  on rcm.sim_claim_adjudication (sim_denial_category);
create index ix_sim_adj_submitted on rcm.sim_claim_adjudication (sim_submission_date);
comment on table rcm.sim_claim_adjudication is
  'SIMULATED adjudication outcomes, money and timeline. The CMS synthetic claims '
  'contain none of this. Does NOT represent real payer behaviour (CLAUDE.md §3, '
  'docs/assumptions.md). Most columns are post-submission — see '
  'docs/simulated_forbidden_columns.md before using any of it as a feature.';

-- ---------------------------------------------------------------------------
-- sim_appeals — one row per (claim, appeal level). Denied claims only.
-- Entirely post-submission: forbidden for Model A, and the label source for
-- Model C (appeal success).
-- ---------------------------------------------------------------------------
drop table if exists rcm.sim_appeals cascade;
create table rcm.sim_appeals (
    sim_appeal_sk               bigint primary key,
    claim_sk                    bigint not null
        references rcm.sim_claim_adjudication (claim_sk),
    clm_id                      text not null,
    sim_appeal_level            smallint not null,
    sim_appeal_filed_date       date not null,
    sim_appeal_decision_date    date not null,
    sim_appeal_outcome          text not null,   -- OVERTURNED | UPHELD
    sim_appeal_disputed_amount  numeric(14, 2) not null,
    sim_appeal_recovered_amount numeric(14, 2) not null,
    sim_appeal_latent_p         numeric(10, 6) not null,  -- VALIDATION ONLY
    sim_provenance              text not null default 'SIMULATED',
    sim_config_version          text not null,
    sim_seed                    bigint not null,
    constraint uq_sim_appeal_grain unique (claim_sk, sim_appeal_level),
    constraint ck_sim_appeal_level   check (sim_appeal_level in (1, 2)),
    constraint ck_sim_appeal_outcome check (sim_appeal_outcome in ('OVERTURNED', 'UPHELD')),
    constraint ck_sim_appeal_amounts check (
        sim_appeal_disputed_amount >= 0 and sim_appeal_recovered_amount >= 0
        and sim_appeal_recovered_amount <= sim_appeal_disputed_amount
    ),
    -- Nothing is recovered on an appeal that was upheld.
    constraint ck_sim_appeal_recovery_iff_overturned check (
        sim_appeal_outcome = 'OVERTURNED' or sim_appeal_recovered_amount = 0
    ),
    constraint ck_sim_appeal_dates check (sim_appeal_filed_date <= sim_appeal_decision_date)
);
create index ix_sim_appeal_claim   on rcm.sim_appeals (claim_sk);
create index ix_sim_appeal_outcome on rcm.sim_appeals (sim_appeal_outcome);
comment on table rcm.sim_appeals is
  'SIMULATED appeals. Invented; the CMS synthetic claims contain no appeals.';

-- ---------------------------------------------------------------------------
-- sim_workflow_events — the event log for process mining. One row per event
-- occurrence, sequenced within a claim by generated timestamp.
-- Post-submission events are forbidden Model A features (CLAUDE.md §4).
-- ---------------------------------------------------------------------------
drop table if exists rcm.sim_workflow_events cascade;
create table rcm.sim_workflow_events (
    sim_event_sk       bigint primary key,
    claim_sk           bigint not null references rcm.fact_inpatient_claim (claim_sk),
    clm_id             text not null,
    sim_event_seq      integer not null,
    sim_event_type     text not null,
    sim_activity       text not null,
    sim_event_date     date not null,
    sim_event_ts       timestamp not null,
    sim_actor_role     text not null,
    sim_appeal_level   smallint not null,   -- 0 when not an appeal event
    sim_touch_minutes  numeric(10, 2) not null,
    sim_provenance     text not null default 'SIMULATED',
    sim_config_version text not null,
    sim_seed           bigint not null,
    constraint uq_sim_event_grain unique (claim_sk, sim_event_seq),
    constraint ck_sim_event_seq     check (sim_event_seq >= 1),
    constraint ck_sim_event_minutes check (sim_touch_minutes >= 0),
    constraint ck_sim_event_level   check (sim_appeal_level between 0 and 2)
);
create index ix_sim_event_claim on rcm.sim_workflow_events (claim_sk);
create index ix_sim_event_type  on rcm.sim_workflow_events (sim_event_type);
create index ix_sim_event_ts    on rcm.sim_workflow_events (sim_event_ts);
comment on table rcm.sim_workflow_events is
  'SIMULATED workflow event log for process mining. Invented; the CMS synthetic '
  'claims contain no workflow events.';

-- ---------------------------------------------------------------------------
-- sim_operating_costs — cost to collect, built bottom-up from the touch minutes
-- in sim_workflow_events so the two tables reconcile by construction.
-- ---------------------------------------------------------------------------
drop table if exists rcm.sim_operating_costs cascade;
create table rcm.sim_operating_costs (
    claim_sk                 bigint primary key
        references rcm.sim_claim_adjudication (claim_sk),
    clm_id                   text not null,
    sim_touch_minutes_total  numeric(12, 2) not null,
    sim_coding_cost          numeric(12, 2) not null,
    sim_submission_cost      numeric(12, 2) not null,
    sim_payment_posting_cost numeric(12, 2) not null,
    sim_denial_rework_cost   numeric(12, 2) not null,
    sim_appeal_cost          numeric(12, 2) not null,
    sim_total_cost_to_collect numeric(12, 2) not null,
    sim_provenance           text not null default 'SIMULATED',
    sim_config_version       text not null,
    sim_seed                 bigint not null,
    constraint ck_sim_cost_nonneg check (
        sim_touch_minutes_total >= 0 and sim_coding_cost >= 0 and sim_submission_cost >= 0
        and sim_payment_posting_cost >= 0 and sim_denial_rework_cost >= 0
        and sim_appeal_cost >= 0 and sim_total_cost_to_collect >= 0
    )
);
comment on table rcm.sim_operating_costs is
  'SIMULATED cost to collect, derived from simulated touch minutes. Invented.';
